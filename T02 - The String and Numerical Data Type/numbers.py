# Ask the user to enter three different integers
integer1 = int(input("Please enter the fisrt integers: "))
integer2 = int(input("Please enter the second integers: "))
integer3 = int(input("Please enter the third integers: "))
print()

# Print out
# The sum of all the numbers
print(integer1 + integer2 + integer3)
print()

# The first number minus the second number
print(integer1 - integer2)
print()

# The third number multiplied by the first number
print(integer3 * integer1)
print()

# The sum of all three numbers divided by the third number
print((integer1 + integer2 + integer3) / integer3)